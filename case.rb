weather = "Hot as Hades"

case weather
	when "Hot as Hades" 
		puts "You gonna melt. No clothes needed."
	when "Pretty Hot" 
		puts "You can wear a bra."
	when "Sweltering" 
		puts "Start reconsidering your layers."
	when "Texas hot" 
		puts "Can't...speak...tooo...hot..."
	when "Warm and breezy" 
		puts "Have a picnic!"
	when "A chill is in the air" 
		puts "Wake up you dreamin."
	else "I don't know what to say."
	end
